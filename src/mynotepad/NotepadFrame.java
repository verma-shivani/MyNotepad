    private void jMenuItem5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem5ActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser=new JFileChooser();
        fileChooser.showOpenDialog(this);
        File selectedFile= fileChooser.getSelectedFile();
              try {
                  fin=new FileInputStream(selectedFile);
                  jLabel1.setText(selectedFile.getAbsolutePath());
                  int data;
                  jTextArea1.setText("");
                  while((data=fin.read())!=-1){
                      jTextArea1.append(""+(char)data);
                  }
                  fout=new FileOutputStream(selectedFile);

              } catch (Exception e) {
                  System.out.println(e);
              }
    }//GEN-LAST:event_jMenuItem5ActionPerformed

    private void jMenuItem6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem6ActionPerformed
        // TODO add your handling code here:
        
        try {
           String data= jTextArea1.getText();
            byte[] bytes = data.getBytes();
            if(fout!=null){
                fout.write(bytes);
                JOptionPane.showMessageDialog(this, "data saved!");
            }
            else{
                JOptionPane.showMessageDialog(this, "Please create a file first");
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        
    }//GEN-LAST:event_jMenuItem6ActionPerformed

